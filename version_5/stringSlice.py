x = "hello vishan welcome to ijse"  #string of characters
print("Original String:", x)
print('---------------------------------------------------------------')


y = x[0:5]  #slicing from index 0 to 5 (5 not included)
print("Sliced String (index 0 to 5):", y)
print('---------------------------------------------------------------')

z = x[:11]  #slicing from start to index 11 (11 not included)
print("Sliced String (start to index 11):", z)
print('---------------------------------------------------------------')